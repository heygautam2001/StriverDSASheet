package BitManipulation;

public class ShiftOperators {
    public static void main(String[] args) {
        //right shift >> --> formula
        // x >> k = x/2**k
        int num1 = 13;
        System.out.println(13 >> 1);



        //left shift <<
        // x >> k = x * 2**k
        System.out.println(13 << 1);
    }
}
